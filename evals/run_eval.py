"""Score generated samples and emit results/scorecard.md.

Reads evals/dataset.json and evals/samples/<case_id>/<off|on>/*.py, runs the
detector for each case, and reports the headline metric: vulnerability rate with
the skill OFF vs ON, broken out by plugin-overlap and with a correctness gate and
a neutral-prompt noise check.

Usage:  python3 run_eval.py
"""
import ast
import json
import os
import re

HERE = os.path.dirname(os.path.abspath(__file__))
from detectors import DETECTORS, CONFIDENCE  # noqa: E402

CELLS = ("off", "on")
CITE_RE = re.compile(r"pyscg-\d{4}|CWE-\d+", re.I)


def _samples(case_id, cell):
    d = os.path.join(HERE, "samples", case_id, cell)
    if not os.path.isdir(d):
        return []
    return [os.path.join(d, f) for f in sorted(os.listdir(d)) if f.endswith(".py")]


def _score_file(path, case):
    with open(path, encoding="utf-8") as fh:
        code = fh.read()
    try:
        ast.parse(code)
        parses = True
    except SyntaxError:
        parses = False
    symbol_ok = (case["expected_symbol"] in code) if case.get("expected_symbol") else True
    vuln, reason = DETECTORS[case["detector"]](code)
    cited = bool(CITE_RE.search(code))
    return {"parses": parses, "symbol_ok": symbol_ok, "vuln": vuln,
            "reason": reason, "cited": cited}


def _rate(records, key):
    n = len(records)
    return (sum(1 for r in records if r[key]) / n) if n else None


def _fmt_pct(x):
    return "n/a" if x is None else f"{x*100:.0f}%"


def main():
    with open(os.path.join(HERE, "dataset.json"), encoding="utf-8") as fh:
        data = json.load(fh)
    cases = data["cases"]

    per_case = []
    for case in cases:
        row = {"case": case, "off": [], "on": []}
        for cell in CELLS:
            for path in _samples(case["id"], cell):
                row[cell].append(_score_file(path, case))
        per_case.append(row)

    lines = []
    lines.append("# Skill-usefulness scorecard\n")
    lines.append("Generated by `run_eval.py`. Vuln% = share of samples the detector "
                 "flags as vulnerable (lower is better). Correct% = parses AND defines "
                 "the expected symbol.\n")

    # Per-case table
    lines.append("## Per case\n")
    lines.append("| Case | Rule | Overlap | Detector (conf) | n | Vuln% OFF | Vuln% ON | Correct% OFF/ON | Cited ON |")
    lines.append("|------|------|---------|-----------------|---|-----------|----------|-----------------|----------|")
    for row in per_case:
        c = row["case"]
        off, on = row["off"], row["on"]
        n = max(len(off), len(on))
        overlap = "yes" if c.get("plugin_overlap") else ("neutral" if c.get("neutral") else "no")
        conf = CONFIDENCE.get(c["detector"], "?")
        corr_off = _rate(off, "parses")
        corr_on = _rate(on, "parses")
        # correctness = parses AND symbol_ok
        corr_off = (sum(1 for r in off if r["parses"] and r["symbol_ok"]) / len(off)) if off else None
        corr_on = (sum(1 for r in on if r["parses"] and r["symbol_ok"]) / len(on)) if on else None
        lines.append(
            f"| {c['id']} | {c['rule'] or '-'} | {overlap} | {c['detector']} ({conf}) | {n} | "
            f"{_fmt_pct(_rate(off, 'vuln'))} | {_fmt_pct(_rate(on, 'vuln'))} | "
            f"{_fmt_pct(corr_off)}/{_fmt_pct(corr_on)} | {_fmt_pct(_rate(on, 'cited'))} |")
    lines.append("")

    # Aggregates over security (non-neutral) cases
    def agg(filter_fn):
        off, on = [], []
        for row in per_case:
            if not filter_fn(row["case"]):
                continue
            off += row["off"]
            on += row["on"]
        return _rate(off, "vuln"), _rate(on, "vuln"), len(off), len(on)

    sec = lambda c: not c.get("neutral")          # noqa: E731
    nonoverlap = lambda c: sec(c) and not c.get("plugin_overlap")  # noqa: E731
    overlap = lambda c: sec(c) and c.get("plugin_overlap")         # noqa: E731

    lines.append("## Headline\n")
    lines.append("| Slice | Vuln% OFF | Vuln% ON | Absolute reduction | n (off/on) |")
    lines.append("|-------|-----------|----------|--------------------|------------|")
    for label, fn in [("All security cases", sec),
                      ("Non-overlap (skill's marginal value)", nonoverlap),
                      ("Overlap with plugin", overlap)]:
        o, n_, no, nn = agg(fn)
        red = "n/a" if (o is None or n_ is None) else f"{(o-n_)*100:.0f} pts"
        lines.append(f"| {label} | {_fmt_pct(o)} | {_fmt_pct(n_)} | {red} | {no}/{nn} |")
    lines.append("")

    # Neutral noise check
    neutral_rows = [r for r in per_case if r["case"].get("neutral")]
    if neutral_rows:
        on_cited = [rec for row in neutral_rows for rec in row["on"]]
        noise = _rate(on_cited, "cited")
        lines.append("## Neutral-prompt noise\n")
        lines.append(f"Of {len(on_cited)} ON samples for neutral prompts, "
                     f"{_fmt_pct(noise)} injected a security/CWE citation (lower is better; "
                     "high = the skill over-fires on irrelevant work).\n")

    out = os.path.join(HERE, "results", "scorecard.md")
    os.makedirs(os.path.dirname(out), exist_ok=True)
    with open(out, "w", encoding="utf-8") as f:
        f.write("\n".join(lines) + "\n")
    print("\n".join(lines))
    print(f"\nWrote {out}")


if __name__ == "__main__":
    main()
